package com.ibenben.gis;

/**
 * 坐标位置点
 * 
 * @author Administrator
 *
 */
public class Point {

	/**
	 * x坐标
	 */
	private double x;
	/**
	 * y坐标点
	 */
	private double y;

	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}

}